package board.model;

public class BoardReplyBean {
	private int idx;
	private int reBbsRef;
	private int memNum;
	private int reRef;
	private int reReStep;
	private int reReLeval;
	private String inputdate;
	private String contents;
	private String nickname;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getReBbsRef() {
		return reBbsRef;
	}
	public void setReBbsRef(int reBbsRef) {
		this.reBbsRef = reBbsRef;
	}
	public int getMemNum() {
		return memNum;
	}
	public void setMemNum(int memNum) {
		this.memNum = memNum;
	}
	public int getReRef() {
		return reRef;
	}
	public void setReRef(int reRef) {
		this.reRef = reRef;
	}
	public int getReReStep() {
		return reReStep;
	}
	public void setReReStep(int reReStep) {
		this.reReStep = reReStep;
	}
	public int getReReLeval() {
		return reReLeval;
	}
	public void setReReLeval(int reReLeval) {
		this.reReLeval = reReLeval;
	}
	public String getInputdate() {
		return inputdate;
	}
	public void setInputdate(String inputdate) {
		this.inputdate = inputdate;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	
}
